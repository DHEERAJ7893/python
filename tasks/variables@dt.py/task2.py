# write a ython program to perform all bitwise operations for given values
a=45
b=65
bitwise_and = a & b
print(bitwise_and)
bitwise_or = a | b
print(bitwise_or)
bitwise_xor = a ^ b
print(bitwise_xor)
bitwise_not = ~a,~b
print(bitwise_not)
bitwise_left_shift = a << b
print(bitwise_left_shift)
bitwise_right_shift = a >> b
print(bitwise_right_shift)